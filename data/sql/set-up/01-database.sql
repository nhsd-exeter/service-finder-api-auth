DROP ROLE IF EXISTS service_finder;
CREATE ROLE service_finder WITH LOGIN ENCRYPTED PASSWORD 'service_finder';
GRANT service_finder TO sfm;

DROP SCHEMA IF EXISTS service_finder CASCADE;
CREATE SCHEMA AUTHORIZATION service_finder;

GRANT CONNECT ON DATABASE service_finder TO service_finder;
ALTER DEFAULT PRIVILEGES GRANT SELECT, INSERT, UPDATE, DELETE ON TABLES TO service_finder;
ALTER DEFAULT PRIVILEGES GRANT USAGE, SELECT ON SEQUENCES TO service_finder;
